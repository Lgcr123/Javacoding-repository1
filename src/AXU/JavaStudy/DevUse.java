package AXU.JavaStudy;

public class DevUse {
    public static void main(String[] args) {
        System.out.println(10/3);
        System.out.println(10.0/3);
        double d=10/3;//3 3转成精度大double 3.0
        System.out.println(d);
        System.out.println("---------");
        //a%b=a-(a/b*b)
        System.out.println(10%3);
        System.out.println(-10%3);
        System.out.println(10%(-3));
        System.out.println(-10%(-3));
    }
}
